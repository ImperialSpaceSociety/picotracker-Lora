	UBLOX_send_message(dummyByte, 1);									// in case only MCU restarted while GPS stayed powered
	HAL_Delay(1000);												// wait for GPS module to be ready
	UBLOX_send_message(setSwBackupMode, 16);							// low consumption at this point

  /* USER CODE END Init */

